package Method.Operators;

public class Boolean {
    public static void main() {
        // < , > , <= , >= , !, !=
        int a = 35;
        int b = 56;
        boolean c = a<b;
        System.out.println(c);
        boolean d = a>b;
        System.out.println(d);
        boolean e = a<=b;
        System.out.println(e);
        boolean f = a>=b;
        System.out.println(f);
        boolean g = a!=b;
        System.out.println(g);
    }
}
