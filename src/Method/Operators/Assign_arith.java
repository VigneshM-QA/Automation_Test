package Method.Operators;

public class Assign_arith {
    public static void main() {
        // = is a assignment operator which assiging the values
        int a = 23;

        // Arithmetic operator + , - , * , / and %
        // Addition, subtraction, multiplication, division and Modulus

        int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println(a==b); // == -> comparison operator and it Results in boolean
    }
}
