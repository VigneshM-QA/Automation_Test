package Method.Loops.For_Loop;

public class For_loop {
    public static void main(String[] args) {
        // Loops --> repeating the block of code
        // Print 1 - 10 using for loop

        // Syntax --> Initialization ; Condition ; updation(increment or decrement)
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
        // Syntax --> Initialization ; Condition ; updation(increment or decrement)
        for (int j = 10; j > 0; j--) {
            System.out.println(j);
        }
    }
}
