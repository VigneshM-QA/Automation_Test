package Method.Loops.Whileloop;

public class while_condition {
    public static void main() {

        // Like same in for loop we use ICU - Initialization, Condition and Updation
        // I
        // while(C)
        // Block of code;
        // U

        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i++;
        }
   }
}
