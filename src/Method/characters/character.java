package Method.characters;

public class character {
    public static void main() {
        char a = 'A';  // single character
        System.out.println(a);

        char b = '*'; // can allow single special character also
        System.out.println(b);

        char c = '\n'; // print in new line
        System.out.println(c);

        char d = '\u1F6A'; // Unicode character
        System.out.println(d);

        String e = "vicky"; // collection of characters including in " "
        System.out.println(e);
    }
}
